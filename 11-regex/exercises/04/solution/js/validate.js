"use strict";

const validate = (str, regExp) => {
  const roles = {
    visa: /^4[0-9]{5}([0-9]{6}|[0-9]{9})[0-9]{4}$/,
    ip: /^(((1[0-9]{0,2})|(2[0-5]{2})|(2[0-9]{1})|([3-9][0,9]{2}))([.-])){1}((0|(1[0-9]{0,2})|(2[0-5]{2})|(2[0-9]{1})|([3-9][0,9]{2}))([.-])){2}((0|1[0-9]{0,2})|(2[0-5]{2})|(2[0-9]{1})|([3-9][0,9]{2}))$/,
    mac: /^([0-9a-fA-F]{2}[:-]){5}([0-9a-fA-F]{2})$/,
  };

  return str.match(roles[regExp]) ? true : false;
};

export default validate;

